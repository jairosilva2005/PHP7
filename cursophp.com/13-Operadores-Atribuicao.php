<?php
	// Operadores de atribuição
	/* Operadores de atribuição podem facilitar e simplificar certas operações */
	$x = 10;
	$y = 12;

	// $x = $x + $y; podemos simplificar esse tipo de operação usando as atribuições - Exemplo:
	$x += $y; // Seria o mesmo que digitar $x = $x + $y
	$x -= $y; // Seria o mesmo que digitar $x = $x - $y
	$x /= $y; // Seria o mesmo que digitar $x = $x / $y
	$x *= $y; // Seria o mesmo que digitar $x = $x * $y
	$x %= $y; // Seria o mesmo que digitar $x = $x % $y
